package ec.edu.espe.sport.model;

/**
 *
 * @author renzo
 */
public class Sport {

    public Sport(int id, String name, String type) {
        this.id = id;
        this.name = name;
        this.type = type;
    }
    
    private int id;
    private String name;
    private String type;
    

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }
    
}
